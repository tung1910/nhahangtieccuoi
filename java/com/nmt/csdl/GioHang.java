package com.nmt.csdl;


/**
 *
 * @author hp
 */
public class GioHang {
    private int maSC;
    private String tenSC;
    private Long donGia;
    private int soLuong;

    /**
     * @return the maSP
     */
    public int getMaSC() {
        return maSC;
    }

    /**
     * @param maSC the maSP to set
     */
    public void setMaSC(int maSC) {
        this.maSC = maSC;
    }

    /**
     * @return the tenSP
     */
    public String getTenSC() {
        return tenSC;
    }

    /**
     * @param tenSC the tenSP to set
     */
    public void setTenSC(String tenSC) {
        this.tenSC = tenSC;
    }

    /**
     * @return the donGia
     */
    public Long getDonGia() {
        return donGia;
    }

    /**
     * @param donGia the donGia to set
     */
    public void setDonGia(Long donGia) {
        this.donGia = donGia;
    }

    /**
     * @return the soLuong
     */
    public int getSoLuong() {
        return soLuong;
    }

    /**
     * @param soLuong the soLuong to set
     */
    public void setSoLuong(int soLuong) {
        this.soLuong = soLuong;
    }
}
